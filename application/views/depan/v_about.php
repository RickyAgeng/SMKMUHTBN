                        <p>SMK Pelayaran Muhammadiyah Tuban berdiri di tahun 1996 dengan nama SPP (Sekolah Pertanian Pembangunan), seiring berjalannya waktu SPP berganti nama menjadi SUPM (Sekolah Usaha  Perikanan Menengah), karena adanya peraturan pemerintah bahwa sekolah kejuruan harus berganti menjadi SMK, maka SUPM berganti nama menjadi SMK Muhammadiyah Tuban. Untuk membranding sekolah agar sekolah ini semakin diminati masyarakat luas maka diganti nama menjadi SMK Pelayaran Muhammadiyah Tuban.</p>
                        <p>Beralamat di Jalan. Hos. Cokroaminoto tersebut menempati area lokasi seluas 11.060 M2. Lahan tersebut sudah bersertifikat atas nama Persyarikatan Muhammadiyah. Meskipun berstatus swasta, SMK Pelayaran Muhammadiyah Tuban pada tahun 2019 telah mendapat pengakuan dari Mendikbudristek sebagai sekolah skala prioritas bidang kemaritiman, sehingga pada tahun 2019 di tetapkan sebagai sekolah Revitalisasi. Pada tahun 2020 di tetapkan sebagai Sekolah COE (Center Of Excellence), selanjutnya tahun 2021 menjadi SMK PK (Pusat Keunggulan ) dan tahun 2022 ditetapkan sebagai SMK PK lanjutan Skema Pemadanan Dukungan.</p>
                        <p>Sejak Tahun 2015 SMK Pelayaran Muhammadiyah Tuban, sudah terverifikasi dari Kementerian Perhubungan dan Kelautan sebagai sekolah yang layak mengadakan Ujian Keahlian Pelaut, Deck and Engine Sertificate of Competency Class II For Fishing Vessel sebagai seorang Chief Officer dan Chief Enginer Tingkat II Kapal Penangkap Ikan.</p>
                        <p>Saat ini  SMK Pelayaran Muhammadiyah Tuban mempunyai empat (4) Kompetensi Keahlian. 1). Teknika Kapal Penangkap Ikan, 2) Nautika Kapal Penangkap Ikan, 3). Agribisnis Pengolahan Hasil Perikanan, 4). Teknik Pemesinan.</p>
                        <p>Bertujuan mencetak sumber daya manusia yang dibekali akhlaq mulia, unggul,  tanggap terhadap kemajuan teknologi sebagai basis peradaban bahari, SMK Pelayaran Muhammadiyah Tuban merupakan sekolah kejuruan pertama yang didirikan oleh persyarikatan Muhammadiyah di bidang kemaritiman.</p>
                        <p>Serapan lulusan di dunia Industri terutama perusahaan Pengolahan Ikan di Jepang dan Kapal Perikanan Jepang, melalui Tee Kay Coorporation Japan  dan Japan Tuna Asosiation, sudah berjalan lebih dari 20 tahun, yang mana setiap tahunnya SMK Pelayaran Muhammadiyah Tuban mampu menempatkan lulusannya ke Jepang lebih dari 50 lulusan.</p>
